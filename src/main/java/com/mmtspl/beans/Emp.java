package com.mmtspl.beans;  
  
public class Emp {  
private int id;  
private String name;  
private float salary;  
private String age;  
  
public int getId() {  
    return id;  
}  
public void setId(int id) {  
    this.id = id;  
}  
public String getName() {  
    return name;  
}  
public void setName(String name) {  
    this.name = name;  
}  
public float getSalary() {  
    return salary;  
}  
public void setSalary(float salary) {  
    this.salary = salary;  
}  
public String getAge() {  
    return age;  
}  
public void setAge(String age) {  
    this.age = age;  
}  
  
}  